x = 10
y = 4
z = 4.5

print('x = ', x, ', y = ', y, ', z = ', z)

print('x + y + z = ', x + y + z)
print('x - 3 = ', x - 3)
print('x * z = ', x * z)
print('x / (y + 2) = ', round(x / (y + 2), 2))
print('x % 2 = ', x % 2)
print('x // y = ', x // y) # // = Integer division
print('x ** 2 = ', x ** 2) # ** = Potentiation

