tupleOne = (0, 1, 2, 3, 4)
tupleTwo = (2, 'margot', 3.1416, True)

print(tupleOne[-1])         # Access to last element
print(tupleOne + tupleTwo)  # Concatenate
print(tupleOne * 3)         # Repetition
print(True in tupleTwo)     # Affiliation

print(len(tupleOne)) # Size
print(min(tupleOne)) # Minimum Value
print(max(tupleOne)) # Maximum Value
print(sum(tupleOne)) # Sum of elements